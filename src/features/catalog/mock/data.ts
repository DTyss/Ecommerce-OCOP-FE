export const getOcopMetadata = (product: Product) => {
  const sequence = Number(product.id.replace(/\D/g, "")) || 1;
  const year = 2023 + (sequence % 3);

  return {
    ocopCertificate: {
      star: product.ocopStar,
      certificateCode: `OCOP-${product.region
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .split(" ")
        .map((part) => part[0])
        .join("")
        .toUpperCase()}-${year}-${String(sequence).padStart(3, "0")}`,
      issuedBy: `UBND vùng ${product.region}`,
      issuedDate: `${year}-${String((sequence % 9) + 1).padStart(2, "0")}-20`,
      image: img("certificate,document,stamp", 600 + sequence),
    },
    traceability: {
      qrValue: `https://ocop-viet.example/trace/${product.id}`,
      batchCode: `LO-${year}-${String(sequence).padStart(4, "0")}`,
      producer: `Cơ sở sản xuất ${product.name}`,
      productionDate: `2026-${String((sequence % 6) + 1).padStart(2, "0")}-15`,
      materialRegion: product.region,
    },
  };
};

const buildProductDetail = (
  productId: string,
  relatedProductIds: string[],
  shippingPolicy = defaultShippingPolicy,
): ProductDetail => {
  const product = MOCK_PRODUCTS.find((item) => item.id === productId);
  const shop = product ? MOCK_PRODUCT_SHOP_SUMMARIES.find((item) => item.id === product.shopId) : undefined;

  if (!product || !shop) {
    throw new Error(`Invalid product detail seed: ${productId}`);
  }

  return {
    ...product,
    ...getOcopMetadata(product),
    shop,
    reviews: MOCK_REVIEWS,
    shippingPolicy,
    relatedProductIds,
  };
};

